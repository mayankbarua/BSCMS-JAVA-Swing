    private void contact_jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_contact_jButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = userdetails_jTable.getSelectedRow();
        if (selectedRow >= 0) {
            User selectedUser = (User) userdetails_jTable.getValueAt(selectedRow, 0);
            Connection connection = (Connection) userdetails_jTable.getValueAt(selectedRow, 4);
            int token = 0;
            if ((connection.getSenderUser().getEmailId().equals(selectedUser.getEmailId()) && connection.getRecieverUser().getEmailId().equals(user.getEmailId())) || (connection.getSenderUser().getEmailId().equals(user.getEmailId()) && connection.getRecieverUser().getEmailId().equals(selectedUser.getEmailId()))) {
                if (connection.getStatus().equals("Connected")) {
                    String sendersEmail = user.getEmailId();
                    String receiversEmail = selectedUser.getEmailId();
                    EmailPage emailPage = new EmailPage(userProcessContainer, network, sendersEmail, receiversEmail);
                    userProcessContainer.add("emailPage", emailPage);
                    CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                    layout.next(userProcessContainer);
                } else {
                    JOptionPane.showMessageDialog(null, "You are not allowed to contact this user as you are blocked");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }


    }//GEN-LAST:event_contact_jButtonActionPerformed

