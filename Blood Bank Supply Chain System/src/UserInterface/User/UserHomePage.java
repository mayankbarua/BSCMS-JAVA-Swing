    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = userdetails_jTable.getSelectedRow();
        if (selectedRow >= 0) {
            User selectedUser = (User) userdetails_jTable.getValueAt(selectedRow, 0);
            int token = 0;
            Connection connection = (Connection) userdetails_jTable.getValueAt(selectedRow, 4);
            if (connection.getSenderUser().getEmailId().equals(selectedUser.getEmailId()) && connection.getRecieverUser().getEmailId().equals(user.getEmailId()) && connection.getHospitalStatus().equals("Received") && connection.getStatus().equals("Pending")) {
                connection.setStatus("Connected");
                getConnections();
                token = 1;
            }
            if (token == 1) {
                JOptionPane.showMessageDialog(null, "Request Approved");
                getConnections();
            }
            if (token == 0) {
                JOptionPane.showMessageDialog(null, "You cannot approve this request");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = userdetails_jTable.getSelectedRow();
        int token = 0;
        if (selectedRow >= 0) {
            User selectedUser = (User) userdetails_jTable.getValueAt(selectedRow, 0);
            Connection connection = (Connection) userdetails_jTable.getValueAt(selectedRow, 4);
            if ((connection.getSenderUser().getEmailId().equals(selectedUser.getEmailId()) && connection.getRecieverUser().getEmailId().equals(user.getEmailId())) || (connection.getSenderUser().getEmailId().equals(user.getEmailId()) && connection.getRecieverUser().getEmailId().equals(selectedUser.getEmailId())) && connection.getHospitalStatus().equals("Received")) {
                if (connection.getStatus().equals("Blocked")) {
                    JOptionPane.showMessageDialog(null, "User is already blocked");
                } else {
                    token = 1;
                    connection.setStatus("Blocked");
                    JOptionPane.showMessageDialog(null, "User Blocked");
                    getConnections();
                }
            }
            if (token == 0) {
                JOptionPane.showMessageDialog(null, "You cannot block this user as this request. Please check connection status");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

